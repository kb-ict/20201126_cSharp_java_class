package operTest201208;

public class OpTest3 {

	public static void main(String[] args) {
		
		// 논리 연산자
		// 제어문(if)에서 많이 활용.
		int num1 = 100, num2 = 200;
		
		// == 같다?
		System.out.println("== 연산자 : " + (num1==num2));
		
		// != 다르다?
		System.out.println("!= 연산자 : " + (num1!=num2));
		
		// 대 소 비교
		// 앞에께 크냐?
		System.out.println("> 연산자 : " + (num1 > num2));
		// 앞에께 작냐?
		System.out.println("< 연산자 : " + (num1 < num2));
		// 크거나 같다?
		System.out.println(">= 연산자 : " + (num1 >= num2));
		// 작거나 같다?
		System.out.println("<= 연산자 : " + (num1 <= num2));
	
	
	}

}
