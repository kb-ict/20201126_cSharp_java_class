package if_20201209;

public class If2 {

	public static void main(String[] args) {

		int num = 200;

		if (num < 100) {
			System.out.println("num가 100보다 작다.");
			System.out.println("2줄이상 코딩 가능");
		} 
		else {
			System.out.println("num가 100보다 크다.");
			System.out.println("2줄이상 코딩 가능");
		}

	}

}
