
public class Operation3 {

	public static void main(String[] args) {
		// 논리 연산자
		// 제어문(if)에서 많이 활용.
		int num1 = 100, num2 = 200;
		// == 같다
		System.out.println("== 연산자: " + (num1 == num2));
		// != 다르다
		System.out.println("!= 연산자: " + (num1 != num2));
		// 대소 비교
		System.out.println("> 연산자: " + (num1 > num2));
		System.out.println("< 연산자: " + (num1 < num2));
		System.out.println(">= 연산자: " + (num1 >= num2));
		System.out.println("<= 연산자: " + (num1 <= num2));
	}

}
